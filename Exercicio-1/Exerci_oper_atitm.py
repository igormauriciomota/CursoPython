# Operadores Aritimeticos

a = 5
b = 3
c = 10
d = 4
e = 7
f = 6
g = 2

# Adição
soma = a + b
print(soma)

# Subtração
menos = c - d
print(menos)

# mutiplicação
multi = e * f
print(multi)

# divição
divis = c / d
print(divis)

# modulo
mod = c % f
print(mod)

# exponencial
expo = f ** g
print(expo)

# Divisão inteira
div_int = f // g
print(div_int)
